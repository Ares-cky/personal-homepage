const toggle = document.querySelector("[data-language-toggle]");
const languageLabel = document.querySelector("[data-language-label]");
const translatableNodes = document.querySelectorAll("[data-i18n]");
let language = "zh";

function applyLanguage(nextLanguage) {
  language = nextLanguage;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = copy[language][key] ?? node.textContent;
  });
  languageLabel.textContent = language === "zh" ? "EN" : "中";
  toggle.setAttribute(
    "aria-label",
    language === "zh" ? "Switch language to English" : "切换到中文"
  );
}

toggle.addEventListener("click", () => {
  applyLanguage(language === "zh" ? "en" : "zh");
});

applyLanguage(language);

const canvas = document.querySelector("#field");
const ctx = canvas.getContext("2d");
const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)");
const pointer = { x: 0.5, y: 0.5, active: false };
let width = 0;
let height = 0;
let deviceScale = 1;
let points = [];
let frameId = 0;

function resizeCanvas() {
  width = window.innerWidth;
  height = window.innerHeight;
  deviceScale = Math.min(window.devicePixelRatio || 1, 2);
  canvas.width = Math.floor(width * deviceScale);
  canvas.height = Math.floor(height * deviceScale);
  canvas.style.width = `${width}px`;
  canvas.style.height = `${height}px`;
  ctx.setTransform(deviceScale, 0, 0, deviceScale, 0, 0);
  buildPoints();
}

function buildPoints() {
  const count = Math.max(28, Math.min(74, Math.floor((width * height) / 18000)));
  points = Array.from({ length: count }, (_, index) => ({
    x: (width * ((index * 37) % 101)) / 100 + Math.sin(index) * 18,
    y: (height * ((index * 53) % 97)) / 100 + Math.cos(index * 1.7) * 18,
    vx: Math.sin(index * 2.31) * 0.18,
    vy: Math.cos(index * 1.91) * 0.18,
    r: 1.2 + ((index * 7) % 11) / 9,
  }));
}

function drawBackground() {
  ctx.clearRect(0, 0, width, height);
  const gradient = ctx.createLinearGradient(0, 0, width, height);
  gradient.addColorStop(0, "rgba(245, 241, 232, 0.65)");
  gradient.addColorStop(0.52, "rgba(255, 250, 240, 0.22)");
  gradient.addColorStop(1, "rgba(222, 215, 204, 0.58)");
  ctx.fillStyle = gradient;
  ctx.fillRect(0, 0, width, height);
}

function drawPoints() {
  const pointerX = pointer.x * width;
  const pointerY = pointer.y * height;

  points.forEach((point, index) => {
    if (!reduceMotion.matches) {
      const pull = pointer.active ? 0.0018 : 0.0006;
      point.vx += (pointerX - point.x) * pull * (index % 3 === 0 ? -1 : 1);
      point.vy += (pointerY - point.y) * pull * (index % 2 === 0 ? -1 : 1);
      point.x += point.vx;
      point.y += point.vy;
      point.vx *= 0.985;
      point.vy *= 0.985;
    }

    if (point.x < -40) point.x = width + 40;
    if (point.x > width + 40) point.x = -40;
    if (point.y < -40) point.y = height + 40;
    if (point.y > height + 40) point.y = -40;

    for (let nextIndex = index + 1; nextIndex < points.length; nextIndex += 1) {
      const next = points[nextIndex];
      const dx = point.x - next.x;
      const dy = point.y - next.y;
      const distance = Math.hypot(dx, dy);
      if (distance < 138) {
        const opacity = (1 - distance / 138) * 0.18;
        ctx.strokeStyle = `rgba(23, 21, 18, ${opacity})`;
        ctx.lineWidth = 1;
        ctx.beginPath();
        ctx.moveTo(point.x, point.y);
        ctx.lineTo(next.x, next.y);
        ctx.stroke();
      }
    }

    const hue =
      index % 4 === 0
        ? "37, 88, 214"
        : index % 4 === 1
          ? "15, 138, 106"
          : index % 4 === 2
            ? "225, 93, 67"
            : "216, 164, 49";
    ctx.fillStyle = `rgba(${hue}, 0.58)`;
    ctx.beginPath();
    ctx.arc(point.x, point.y, point.r, 0, Math.PI * 2);
    ctx.fill();
  });
}

function render() {
  drawBackground();
  drawPoints();
  frameId = window.requestAnimationFrame(render);
}

window.addEventListener("resize", resizeCanvas);
window.addEventListener("pointermove", (event) => {
  pointer.x = event.clientX / Math.max(width, 1);
  pointer.y = event.clientY / Math.max(height, 1);
  pointer.active = true;
});
window.addEventListener("pointerleave", () => {
  pointer.active = false;
});

reduceMotion.addEventListener("change", () => {
  window.cancelAnimationFrame(frameId);
  render();
});

resizeCanvas();
render();
